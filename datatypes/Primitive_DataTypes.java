/*
Write a program to understand the concept of primitive_datatypes
Primitive_DataTypes:Primitive Data Types are those datatypes which are predefined in the complier
KeyPoints:-The size of the primitive_datatypes is always fixed..
eg:-int,byte,short,long,etc
*/
package datatypes;
public class Primitive_DataTypes
{
    public static void main(String[] args)
    {
        byte b=10;
        short s=2000;
        int i=10000;
        long l=10000000L;
        float f=10.2f;
        double d=19.99;
        char c='A';
        boolean bool=true;

        System.out.println("byte:" +b);
        System.out.println("short:" +s);
        System.out.println("integer:" +i);
        System.out.println("long:" +l);
        System.out.println("float:" +f);
        System.out.println("char:" +c);
        System.out.println("boolean:" +bool);
    }

}
