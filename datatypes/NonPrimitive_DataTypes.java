/* Non Primitive_DataTypes:-Non_Primitive DataTypes are those datatypes which are derived from the primitive ddatypes
KeyPoints:The size of the non_primitive_datatypes is not fixed.
eg:-Arrays,Strings,etc
 */
package datatypes;
public class NonPrimitive_DataTypes
{
    public static void main(String[] args)
    {
        //1.Arrays:used to store values of same type
        int[]numbers={10,20,30};
        System.out.println("First_Number: "+numbers[0]);
        System.out.println("Second_Number: "+numbers[1]);
        System.out.println("Third_Number: "+numbers[2]);

        //2.Strings:used to store text
        String name="Nikhil";
        System.out.println("Name: "+name);
    }
}
