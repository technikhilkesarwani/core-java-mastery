/*
Write a program to understand the concept of WhileLoop Example
Looping Statements:-Also known as iteration statements allows us to repeat block of code multiple times.
eg:-while loop,do-while loop,for loop
WhileLoop:-While loop is used to repeat the block of the code as long specific condition is true
KeyPoints:It is useful when we do not know how many times the statement is going to be executed.
 */
package controlflow;
public class WhileLoopExample
{
    public static void main(String[] args)
    {
        int i=0;//Initialisation
        while(i<5)//Condition
        {
            System.out.println("Namaste_Duniya");
            i++;//Iteration
        }
    }
}
