/*
Write a program to understand the concept of ForLoop Example
ForLoop:-for loop is used to repeat a block od code specific number of times.
It is useful when we know exactly how many times we need to repeat a task
 */
package controlflow;
public class ForLoopExample
{
    public static void main(String[] args)
    {
        //for(initialisation;condition;increment)
        for(int i=0;i<5;i++)
        {
            //loop body:print "Namaste Duniya";
            System.out.println("Namaste_Duniya");
        }
    }
}
