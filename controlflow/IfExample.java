/* Write a program to understand the if statement
Control Statements:-Control Statements are those statements which are used to control
the flow of execution of the programs depending upon the conditions present...
i)Decision-Making Statements eg:-if,if-else,if-else-if,switch
ii)Looping&Iteration Statements eg:-while loop,do-while loop,for,foreach loop
iii)Jump Statements eg:-break,continue,return
if statement:- It checks the condition.If the condition is true then if block will be executed .
*/
