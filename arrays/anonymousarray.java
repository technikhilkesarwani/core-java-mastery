//Write a program to understand the concepts of anonymous arrays
/* Array without name known as anonymous arrays used only for once a time */

package arrays;
public class anonymousarray
{
    public static void main(String[] args)
    {
        //No need of declaration of the array
        //Arrays without name:Anonymous Arrays
        System.out.println(new int []{10,20,30,40}[2]);
    }
}
